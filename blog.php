                        <h2>Mobile Legends Bang bang</h2>

                        <p>Mobile Legends merupakan sebuah game bertema 5v5 MOBA. Di dalam permainan para <i>gamer</i> ditantang dalam serangkaian level peningkatan serta dituntut untuk lihai meracik strategi dan peralatan <i>(gear)</i> agar Hero yang dimainkan dapat terus berkembang dengan baik. Dikembangkan oleh developer Moonton membuat kepopuleran Mobile Legends meledak begitu cepat.</p>
                        <p>Secara umum yang membuat game ini begitu menantang adalah pemain bisa memilih Hero atau karakter yang begitu beragam untuk melawan pemain lainnya dengan bertarung secara online <i>(Player vs Player)</i>. Saat ini ada 21 Hero atau Champion yang siap dimainkan, bahkan angka tersebut akan terus bertambah seiring dengan versi pembaruan game.</p>
                        <p>Sebagai game bergenre MOBA, Mobile Legends dapat dimainkan dalam mode <i>Match Up</i> hingga mode peringkat <i>(Ranked)</i>. Selain itu, yang membuat game ini ramai pemain adalah kemudahan navigasi untuk mengendalikan Hero dan sang pengembang game sangat rutin dalam merilis versi pembaruan.</p>
                        <p>Game yang sangat mirip dengan DOTA 2 dan LoL ini dapat dijalankan pada perangkat mobile dengan sistem operasi Android dan iOS. Spesifikasi perangkat yang direkomendasikan adalah RAM minimum 1 GB dan CPU 4 Core. Untuk menunjang kenyamanan dalam bermain game ini membutuhkan jaringan koneksi yang kuat dan stabil.</p>
                        <h2>Fitur Game Mobile Legends</h2>
                        <p>Beberapa fitur Mobile Legends diantaranya adalah:<br />
                        <ul>
                        <li>Pertempuran 5v5 melawan pemain sungguhan, bukan bot. Permainan menggunakan peta klasik MOBA.</li>
                        <li>Banyak pilihan Hero atau Champions yang dibagi menjadi beberapa jenis yaitu petarung <i>(Fighter)</i>, pembunuh <i>(Assassin)</i>, penyihir <i>(Mage)</i>, pendukung <i>(Support)</i>, <i>Tank</i>, dan penembak <i>(Marksman)</i>.</li>
                        <li>Kemenangan dapat diraih dengan kerjasama tim &amp; strategi.</li>
                        <li>Pertarungan adil. Menang dan kalah tergantung <i>skill</i> pemain dalam memilih Hero, meracik strategi, kombinasi item, <i>ability</i>, dan <i>emblem</i>.</li>
                        <li>Kontrol sederhana dan mudah. Hanya dengan sentuhan 2 jari untuk mengendalikan Hero dalam pertarungan.</li>
                        <li>Bantuan <i>Offline AI</i>. Untuk menghindari permainan 5 melawan 4 maka pihak developer membuat sistem AI yang membantu pemain yang tiba-tiba keluar dari game karena koneksi yang turun.</li>
                        <li><i>Auto Reconnect</i>. Dengan fitur ini pemain tidak perlu khawatir saat tiba-tiba jaringan melemah atau jelek ketika sedang bermain karena game akan otomatis dapat dimainkan lagi dalam waktu beberapa detik ketika sudah kembali terhubung internet dan tentunya pertandingan belum selesai.</li>
                        </ul>
                        Saat ini game Mobile Legends menjadi game online yang paling populer di Indonesia. Hal tersebut didukung oleh makin berkembangnya komunitas eSport di tanah air.<br />
